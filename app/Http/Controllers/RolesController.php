    public function processroleupdate(Request $request,$id )
    {
        $user=User::findOrFail($id);

        foreach ($user->getrolenames() as $role) {
            $user->removeRole($role);
        }
        $user->assignRole($request->roles);
        $user->save();
        return redirect('/users');
    }

